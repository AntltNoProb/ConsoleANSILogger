function insertAfter(node, content) {
    if (node === undefined) {
        return document.querySelector('head').appendChild(content);
    }
    return node.parentNode.insertBefore(content, node.nextSibling);
}

function createScriptElem(src) {
    const rt = document.createElement('script');
    rt.setAttribute('type', 'text/javascript');
    rt.setAttribute('src', src);
    return rt;
}

function importJS(src, node, debug) {
    return new Promise((res, rej)=>{
        const elem = createScriptElem(src);
        elem.onload = ()=>{res(src)};
        elem.onerror = ()=>{rej(src)}
        insertAfter(node, elem);
    }).then((src)=>{
        if (debug) {
            console.debug('script load: ', src);
        }
    }).catch((src)=>{
        console.warn('importing resource failed: %c%s', 
            'font-style: italic;', 
            src);
    })
}

function allIn(node) {
    return Promise.allSettled([
        importJS('example/abcfn.js', node),
        importJS('example/temp.js', node),
        importJS('example/blue1911.js', node),
        importJS('example/blue1911code.js', node),
        importJS('example/importover.js', node),
    ]);
}

